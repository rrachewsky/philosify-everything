// Script to balance quiz option lengths using Gemini API
// Run with: node scripts/balance-quiz-options.js
// Generates SQL UPDATE statements to rewrite short wrong answers

const fs = require('fs');

async function main() {
  const GEMINI_KEY = process.env.GEMINI_API_KEY;
  if (!GEMINI_KEY) {
    console.error('Set GEMINI_API_KEY env var');
    process.exit(1);
  }

  const s1 = fs.readFileSync('migrations/quiz_seed.sql', 'utf8');
  const s2 = fs.readFileSync('migrations/quiz_seed_part2.sql', 'utf8');
  const all = s1 + s2;

  // Extract full question blocks
  const blockRe = /\('([a-z_]+)',\s*(\d+),\s*'((?:[^']|'')*)',\s*'\[((?:[^']|'')*)\]',\s*'([a-d])',\s*'((?:[^']|'')*)',\s*'\{((?:[^']|'')*)\}'\)/g;
  let m;
  const questions = [];

  while (m = blockRe.exec(all)) {
    try {
      const questionText = m[3].replace(/''/g, "'");
      const optionsRaw = '[' + m[4].replace(/''/g, "'") + ']';
      const options = JSON.parse(optionsRaw);
      const correctAnswer = m[5];
      const explanation = m[6].replace(/''/g, "'");
      const wrongExplRaw = '{' + m[7].replace(/''/g, "'") + '}';

      const correctOpt = options.find(o => o.id === correctAnswer);
      const wrongOpts = options.filter(o => o.id !== correctAnswer);
      const correctWords = correctOpt?.text?.split(/\s+/).length || 0;
      const avgWrongWords = wrongOpts.reduce((s, o) => s + (o.text?.split(/\s+/).length || 0), 0) / wrongOpts.length;
      const ratio = correctWords / (avgWrongWords || 1);

      if (ratio > 1.3) {
        questions.push({
          questionText,
          options,
          correctAnswer,
          correctWords,
          avgWrongWords: avgWrongWords.toFixed(1),
          ratio: ratio.toFixed(2),
        });
      }
    } catch (e) {
      // skip parse errors
    }
  }

  console.log(`Found ${questions.length} questions needing rebalancing\n`);

  const output = ['-- Quiz option length rebalancing\n-- Generated by scripts/balance-quiz-options.js\n'];
  const BATCH = 5;

  for (let i = 0; i < questions.length; i += BATCH) {
    const batch = questions.slice(i, i + BATCH);
    console.log(`Processing batch ${Math.floor(i / BATCH) + 1}/${Math.ceil(questions.length / BATCH)}...`);

    const prompt = `You are rewriting quiz answer options to make all options approximately the same word count. The correct answer must NOT change in meaning. The wrong answers must be rewritten to be plausible and similar in length to the correct answer.

Return ONLY a JSON array with the rewritten options for each question. Each element: {"question": "original question text", "options": [{"id": "a", "text": "..."}, ...]}

Questions to rebalance:
${batch.map((q, idx) => `
Question ${idx + 1}: "${q.questionText}"
Correct answer (${q.correctAnswer}): "${q.options.find(o => o.id === q.correctAnswer).text}" (${q.correctWords} words)
Wrong answers: ${q.options.filter(o => o.id !== q.correctAnswer).map(o => `${o.id}: "${o.text}" (${o.text.split(/\s+/).length}w)`).join(', ')}
Target: all options ~${q.correctWords} words each
`).join('\n')}

RULES:
- Keep correct answer text unchanged or very close to original
- Rewrite WRONG answers to be similar length (within ±2 words of correct answer)
- Wrong answers must remain plausible but clearly wrong to someone who knows the subject
- Keep option IDs unchanged
- Return valid JSON only, no markdown fences`;

    try {
      const res = await fetch(
        `https://generativelanguage.googleapis.com/v1beta/models/gemini-2.0-flash:generateContent?key=${GEMINI_KEY}`,
        {
          method: 'POST',
          headers: { 'Content-Type': 'application/json' },
          body: JSON.stringify({
            contents: [{ parts: [{ text: prompt }] }],
            generationConfig: { temperature: 0.2, maxOutputTokens: 4096 },
          }),
        },
      );

      const data = await res.json();
      const raw = data.candidates?.[0]?.content?.parts?.[0]?.text || '';
      const jsonStr = raw.replace(/```json\n?/g, '').replace(/```\n?/g, '').trim();
      const results = JSON.parse(jsonStr);

      for (const result of results) {
        const q = batch.find(b => b.questionText === result.question);
        if (!q || !result.options) continue;

        const optionsJson = JSON.stringify(result.options).replace(/'/g, "''");
        const whereQuestion = q.questionText.replace(/'/g, "''");
        output.push(`UPDATE quiz_questions SET options = '${optionsJson}'::jsonb WHERE question = '${whereQuestion}';`);
      }
    } catch (e) {
      console.error(`  Batch failed:`, e.message);
    }

    // Rate limit
    if (i + BATCH < questions.length) await new Promise(r => setTimeout(r, 1000));
  }

  const sqlFile = 'migrations/quiz_balance_options.sql';
  fs.writeFileSync(sqlFile, output.join('\n\n') + '\n');
  console.log(`\nDone! ${output.length - 1} UPDATE statements written to ${sqlFile}`);
}

main().catch(console.error);
