/**
 * Get localized error message
 * @param {string} errorKey - Error key from ERROR_MESSAGES_I18N
 * @param {string} lang - Language code (ISO 639-1)
 * @returns {string} Localized error message (fallback to English)
 */
export function getLocalizedError(errorKey, lang = 'en') {
  const messages = ERROR_MESSAGES_I18N[errorKey];
  if (!messages) {
    console.warn(`[i18n] Unknown error key: ${errorKey}`);
    return errorKey; // Return key as-is if not found
  }
  return messages[lang] || messages.en || errorKey;
}

/**
 * Create localized error response object
 * @param {string} errorKey - Error key
 * @param {string} lang - Language code
 * @param {object} additionalData - Additional data to include
 * @returns {object} Error response with localized message
 */
export function createLocalizedErrorResponse(errorKey, lang = 'en', additionalData = {}) {
  return {
    error: getLocalizedError(errorKey, lang),
    code: errorKey,
    ...additionalData,
  };
}
